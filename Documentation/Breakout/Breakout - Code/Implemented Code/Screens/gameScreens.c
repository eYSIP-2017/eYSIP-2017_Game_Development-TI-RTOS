/*

 * Function Name: gameScreenRefresh()

 * Input: None

 * Output: None

 * Description: This function refreshes an in progress gameplay screen, based on which bricks are still intact and
                which are not. Usually called upon REENTRY substate of the GAMEPLAY state.

 * Example Call: gameScreenRefresh()

 */
void gameScreenRefresh(void)
{
    unsigned char i = 0, j = 0, k = 0;

    glcd_clearDisplay();
    for(i = 0; i < 4; i++) // Cycle through rows
    {
        for(j = 0; j < 8; j++) // Cycle through columns
        {
            // Update block at its position based on its type
            if(block[k] == BLOCK_HARD)
                blockDisplay(i, j, block_hardv2);
            else if(block[k] == BLOCK_MED)
                blockDisplay(i, j, block_medv2);
            else if(block[k] == BLOCK_EASY)
                blockDisplay(i, j, block_easyv2);
            else if(block[k] == BLOCK_MAGIC1)
                blockDisplay(i, j, block_magic1v2);
            else if(block[k] == BLOCK_MAGIC2)
                blockDisplay(i, j, block_magic2v2);
            k++;
        }
    }
}
/*

 * Function Name: screenFlash()

 * Input: *image

 * Output: None

 * Description: This function displays the image given to it in the form of a flashing screen with a period
                of 1 second.

 * Example Call: screenFlash(oneLifeScreen)

 */
void screenFlash(unsigned char *image)
{
    unsigned char flashCount;
    // Cycle three times
    for(flashCount = 0; flashCount < 3; flashCount++)
    {
        glcd_write(image);
        millis(500);
        glcd_clearDisplay();
        millis(500);
        glcd_write(image);
    }
}
